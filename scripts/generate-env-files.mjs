#!/usr/bin/env node
/**
 * Generate/refresh .env and .env.local from repo references + secrets sources.
 * - Collects keys from .env.example, .env, and process.env usage in code.
 * - Preserves existing non-empty values by default.
 * - Reads real values from:
 *   1) --secrets <path> (e.g. .env.secrets)
 *   2) --vercel <path> (Vercel env export file)
 * - Never invents or fetches secrets.
 *
 * Usage:
 *   node scripts/generate-env-files.mjs --write
 *   node scripts/generate-env-files.mjs --write --secrets .env.secrets --vercel vercel.env
 *   node scripts/generate-env-files.mjs --write --no-preserve
 *   node scripts/generate-env-files.mjs --write --only-local
 */
import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const ENV_FILES = [".env", ".env.local", ".env.example"];
const CODE_DIRS = ["api", "lib", "src", "scripts", "docs"];

const argv = process.argv.slice(2);
const args = new Set(argv);
const shouldWrite = args.has("--write");
const preserveExisting = !args.has("--no-preserve");
const onlyLocal = args.has("--only-local");

function argValue(flag) {
  const idx = argv.indexOf(flag);
  if (idx === -1) return "";
  return argv[idx + 1] || "";
}

const secretsPath =
  argValue("--secrets") || path.join(ROOT, ".env.secrets");
const vercelPath =
  argValue("--vercel") || path.join(ROOT, "vercel.env");

function readFileIfExists(p) {
  try {
    return fs.readFileSync(p, "utf8");
  } catch {
    return "";
  }
}

function parseEnvFile(content) {
  const map = new Map();
  const lines = content.split(/\r?\n/);
  for (const line of lines) {
    if (!line || line.trim().startsWith("#")) continue;
    const idx = line.indexOf("=");
    if (idx <= 0) continue;
    const key = line.slice(0, idx).trim();
    const val = line.slice(idx + 1);
    if (key) map.set(key, val);
  }
  return map;
}

function parseEnvFileFromPath(p) {
  const content = readFileIfExists(p);
  return parseEnvFile(content);
}

function collectKeysFromEnvFiles() {
  const keys = new Set();
  for (const f of ENV_FILES) {
    const content = readFileIfExists(path.join(ROOT, f));
    const parsed = parseEnvFile(content);
    for (const k of parsed.keys()) keys.add(k);
  }
  return keys;
}

function collectKeysFromCode() {
  const keys = new Set();
  const envAccess = /\bprocess\.env\.([A-Z0-9_]+)\b/g;
  const mustEnv = /\bmustEnv\(\s*["']([A-Z0-9_]+)["']\s*\)/g;

  for (const dir of CODE_DIRS) {
    const abs = path.join(ROOT, dir);
    if (!fs.existsSync(abs)) continue;
    const entries = fs.readdirSync(abs, { withFileTypes: true });
    const stack = entries.map((e) => path.join(abs, e.name));

    while (stack.length) {
      const p = stack.pop();
      if (!p) continue;
      const stat = fs.statSync(p);
      if (stat.isDirectory()) {
        const kids = fs.readdirSync(p, { withFileTypes: true });
        for (const k of kids) stack.push(path.join(p, k.name));
        continue;
      }

      const ext = path.extname(p).toLowerCase();
      if (![".js", ".jsx", ".mjs", ".ts", ".tsx", ".md"].includes(ext)) continue;

      const content = readFileIfExists(p);
      let m;
      while ((m = envAccess.exec(content))) keys.add(m[1]);
      while ((m = mustEnv.exec(content))) keys.add(m[1]);
    }
  }

  return keys;
}

function mergeKeys(keys) {
  return Array.from(keys).sort((a, b) => a.localeCompare(b));
}

function buildEnvFile(keys, existingMap, vercelMap, secretsMap) {
  const lines = [];
  lines.push("# Generated by scripts/generate-env-files.mjs");
  lines.push("# Fill values in .env.local or Vercel Environment Variables");
  lines.push("# Avoid putting secrets in VITE_* variables (they are client-exposed).");
  lines.push("");

  const defaults = new Map([
    ["PORT", "3002"],
    ["BASE_URL", "http://localhost:3002"],
    ["SETUP_URL", "https://www.zomorodmedical.com/api/setup"],
    ["VITE_BUILD_ID", "local"],
  ]);

  function getBaseUrl() {
    const fromSecrets = secretsMap.get("BASE_URL");
    const fromVercel = vercelMap.get("BASE_URL");
    const fromExisting = existingMap.get("BASE_URL");
    const fromDefault = defaults.get("BASE_URL");
    return (
      (fromSecrets && String(fromSecrets)) ||
      (fromVercel && String(fromVercel)) ||
      (fromExisting && String(fromExisting)) ||
      (fromDefault && String(fromDefault)) ||
      ""
    );
  }

  const baseUrl = getBaseUrl().replace(/\/$/, "");

  for (const key of keys) {
    const fromSecrets = secretsMap.get(key);
    const fromVercel = vercelMap.get(key);
    const existing = existingMap.get(key);

    if (typeof fromSecrets === "string" && fromSecrets !== "") {
      lines.push(`${key}=${fromSecrets}`);
    } else if (typeof fromVercel === "string" && fromVercel !== "") {
      lines.push(`${key}=${fromVercel}`);
    } else if (preserveExisting && typeof existing === "string" && existing !== "") {
      lines.push(`${key}=${existing}`);
    } else if (key === "VITE_API_BASE" && baseUrl) {
      lines.push(`${key}=${baseUrl}/api`);
    } else if (defaults.has(key)) {
      lines.push(`${key}=${defaults.get(key)}`);
    } else {
      lines.push(`${key}=`);
    }
  }

  lines.push("");
  return lines.join("\n");
}

function main() {
  const keys = new Set();
  for (const k of collectKeysFromEnvFiles()) keys.add(k);
  for (const k of collectKeysFromCode()) keys.add(k);

  const sortedKeys = mergeKeys(keys);

  const envPath = path.join(ROOT, ".env");
  const envLocalPath = path.join(ROOT, ".env.local");

  const envExisting = parseEnvFile(readFileIfExists(envPath));
  const envLocalExisting = parseEnvFile(readFileIfExists(envLocalPath));

  const secretsMap = fs.existsSync(secretsPath) ? parseEnvFileFromPath(secretsPath) : new Map();
  const vercelMap = fs.existsSync(vercelPath) ? parseEnvFileFromPath(vercelPath) : new Map();

  const envOut = buildEnvFile(sortedKeys, envExisting, vercelMap, secretsMap);
  const envLocalOut = buildEnvFile(sortedKeys, envLocalExisting, vercelMap, secretsMap);

  if (shouldWrite) {
    if (!onlyLocal) {
      fs.writeFileSync(envPath, envOut, "utf8");
    }
    fs.writeFileSync(envLocalPath, envLocalOut, "utf8");
    console.log("Wrote .env and .env.local");
    console.log(`Used secrets file: ${fs.existsSync(secretsPath) ? secretsPath : "not found"}`);
    console.log(`Used Vercel env file: ${fs.existsSync(vercelPath) ? vercelPath : "not found"}`);
  } else {
    console.log("Dry run. Use --write to update files.");
  }
}

main();
